package switchCase;

public class month {
  public static void main(String[] args) {
    int month = 4;
     switch (month) {
       case 1:
      System.out.println("january");

        break;
        case 2:
      System.out.println("fabruary");
      break;

        case 3:
      System.out.println("march");
      break;

      case 4:
      System.out.println("april");
        break;

      default:
        break;
     }
  }

}
