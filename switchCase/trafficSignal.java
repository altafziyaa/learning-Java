package switchCase;

public class trafficSignal {
  public static void main(String[] args) {
    String signal = "green";

    switch (signal) {
      case "red":
      System.out.println("signal red");
      break;
      case "green":
      System.out.println("signal red");
      break;
      case "yellow":
      System.out.println("signal red");
      break;
      default:
        break;
    }
  }
}
